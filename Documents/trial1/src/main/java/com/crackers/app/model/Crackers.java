package com.crackers.app.model;
public class Crackers 
{
	private long crackersId;
	private String crackersName;
	private String crackersCategory;
	private double crackersPrice;


	public long getCrackersId() 
	{
		return crackersId;
	}

	public void setCrackersId(long crackersId) 
	{
		this.crackersId = crackersId;
	}

	public String getCrackersName() 
	{
		return crackersName;
	}

	public void setCrackersName(String crackersName) 
	{
		this.crackersName = crackersName;
	}

	public String getCrackersCategory() 
	{
		return crackersCategory;
	}

	public void setCrackersCategory(String crackersCategory) 
	{
		this.crackersCategory = crackersCategory;
	}

	public double getCrackersPrice() 
	{
		return crackersPrice;
	}

	public void setCrackersPrice(double crackersPrice) 
	{
		this.crackersPrice = crackersPrice;
	}
}

